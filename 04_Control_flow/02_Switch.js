const month = "April";

switch (month) {
    case "jan":
        console.log("January");
        break;
        
    case "feb":
        console.log("feb");
        break;

    case "march":
        console.log("March");
        break;

    case "April":
        console.log("April");
        break;

    default:
        console.log("Default case mathch");
        break;
}
